// ST77916 QSPI LCD (360x360 round) for the Waveshare ESP32-S3-Touch-LCD-1.85C.
//
// LovyanGFX has no native ST77916, but its Panel_AMOLED base already speaks the exact QSPI protocol
// this controller uses: commands framed as {0x02,0x00,cmd,0x00} (write_cmd) and pixels via the
// 0x32 / 0x2C (RAMWR) stream — see Panel_AMOLED.cpp. So we subclass Panel_AMOLED and supply two
// board-specific overrides:
//   - getInitCommands(): the panel-vendor init sequence (lifted verbatim from the shipping xiaozhi
//     firmware's `vendor_specific_init_new` for this exact board), translated to LGFX's
//     {cmd, num_args [|0x80=delay], args..., [delay_ms]} / 0xFF,0xFF-terminated list format.
//   - setBrightness(): Panel_AMOLED drives brightness via LCD cmd 0x51 (AMOLED DBV), but the 1.85C is
//     a backlit LCD whose backlight is a real GPIO (5, PWM). Route brightness back to the attached
//     Light_PWM (Panel_Device's behaviour) so the Dim service actually controls the backlight.
